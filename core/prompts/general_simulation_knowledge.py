ERUPTIVE_SIMULATION = """\
**Eruptive simulation*:
An eruptive simulation (סימולציה מתפרצת) is a covert, real-time leadership simulation in which the participant (the cadet) is not aware in advance that they are being evaluated.
The simulation is designed to emerge unexpectedly from routine, creating a situation that feels real and unplanned. The cadet must respond immediately, without prior preparation, relying on their instincts, judgment, and leadership approach.
The purpose of this type of simulation is to expose the cadet’s authentic behavior under pressure, including:
* Decision-making in conditions of uncertainty
* Leadership and command presence
* Value-based judgment
* Interpersonal conduct with subordinates, peers, and superiors

An eruptive simulation is characterized by:
* Concealment – the cadet does not know the simulation is taking place
* Immediacy – no preparation time; response is in real time
* Realism – scenarios closely reflect situations officers may encounter in practice
* Focused tension – the situation includes a clear dilemma (command-related, ethical, or interpersonal)
* Short duration – typically a brief interaction that develops quickly

The simulation is usually initiated by a natural trigger event (e.g., a conversation, incident, or unexpected report) that integrates seamlessly into the cadet’s routine and evolves dynamically based on their responses.
The emphasis is not on “performing correctly,” but on revealing the cadet’s natural patterns of thinking and behavior in a leadership situation.

## Examples (Note these are only possible narratives for eruptive simulations):
### Example 1:
It’s the morning, and the team is gathered outside for their first class of the day. The simulated cadet is standing with the group.
One of the simulator cadets arrives late, slightly out of breath, and joins the group quietly.
Another simulator cadet says, loud enough to be heard: "Of course he’s late again... some people just get away with everything."
The late cadet responds immediately: "What do you want from me?"
The group goes quiet and looks toward the simulated cadet. No instructor is present.
The late cadet turns to the other simulator cadet: "I have my reasons. It’s not like I just overslept."
The other simulator cuts in: "Yeah? That’s what everyone says. Funny how it’s always you with the 'special circumstances.' Maybe just admit you don’t take this seriously."
The situation is still informal - but clearly escalating. The simulated cadet is now implicitly expected to respond.

Note, this can be used to simulate the leader cadet of the week - there is no reason why they will be the one expected to take action.

### Example 2:
The simulated cadet is in the middle of delivering a lesson (ideally a sensitive or a political subject).
One simulator cadet interrupts with a comment about the content.
A few minutes later, another simulator cadet interrupts disagreeing with the first simulator.
They begin responding to each other - the side conversation grows and does not allow the lesson to continue.

From nearby, a third simulator cadet suggests: "Just stop them or send them out, otherwise this won’t end."

Note, this requires the simulated cadet is given a lesson to deliver.

## General Notes:
1. Try to be creative - this type of simulations can be integrated almost anywhere with enough creativity.
2. You can use variable actors - The base commander, the discipline sergent, the kitchen worker, etc.
3. The most important part is to seamlessly integrate the simulation, otherwise the simulated cadet will suspect this is a simulation!  
"""

